    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {

        $validator = Validator::make($request->all(), [
            'password' => 'min:8|required_with:konfirm_password| same:konfirm_password| nullable',
            'konfirm_password'  => 'min:8| nullable'
        ]);
        if ($validator->fails()) {
            return redirect()->back()->withErrors($validator)->withInput();
        }
        try {
            $user = Auth::user();
            $user->name = $request->name;
            $user->email = $request->email;
            if ($request->password) {
                $user->password = Hash::make($request->password);
            }
            $user->save();
            return redirect()->back()->with('success', 'Berhasil Memperbaharui data');
        } catch (\Exception $e) {
            return redirect()->back()->with('error', 'Gagal Memperbaharui data ' . $e);
        }
    }

